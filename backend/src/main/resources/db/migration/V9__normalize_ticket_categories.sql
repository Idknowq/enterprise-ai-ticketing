UPDATE tickets
SET category = CASE UPPER(TRIM(category))
    WHEN 'REMOTE_ACCESS' THEN 'REMOTE_ACCESS'
    WHEN 'VPN' THEN 'REMOTE_ACCESS'
    WHEN 'VPN_ISSUE' THEN 'REMOTE_ACCESS'
    WHEN 'IDENTITY_ACCOUNT' THEN 'IDENTITY_ACCOUNT'
    WHEN 'PASSWORD_MFA' THEN 'PASSWORD_MFA'
    WHEN 'PASSWORD_RESET' THEN 'PASSWORD_MFA'
    WHEN 'MFA' THEN 'PASSWORD_MFA'
    WHEN 'SSO' THEN 'PASSWORD_MFA'
    WHEN 'ACCESS_REQUEST' THEN 'ACCESS_REQUEST'
    WHEN 'ACCESS' THEN 'ACCESS_REQUEST'
    WHEN 'PERMISSION' THEN 'ACCESS_REQUEST'
    WHEN 'EMAIL_COLLABORATION' THEN 'EMAIL_COLLABORATION'
    WHEN 'DEVICE_HARDWARE' THEN 'DEVICE_HARDWARE'
    WHEN 'DEVICE' THEN 'DEVICE_HARDWARE'
    WHEN 'DEVICE_SUPPORT' THEN 'DEVICE_HARDWARE'
    WHEN 'OPERATING_SYSTEM' THEN 'OPERATING_SYSTEM'
    WHEN 'SOFTWARE_APPLICATION' THEN 'SOFTWARE_APPLICATION'
    WHEN 'SOFTWARE' THEN 'SOFTWARE_APPLICATION'
    WHEN 'SOFTWARE_LICENSE' THEN 'SOFTWARE_APPLICATION'
    WHEN 'NETWORK_CONNECTIVITY' THEN 'NETWORK_CONNECTIVITY'
    WHEN 'SECURITY_INCIDENT' THEN 'SECURITY_INCIDENT'
    WHEN 'DATA_BACKUP_RECOVERY' THEN 'DATA_BACKUP_RECOVERY'
    WHEN 'CLOUD_INFRASTRUCTURE' THEN 'CLOUD_INFRASTRUCTURE'
    WHEN 'DATABASE_DATA_PLATFORM' THEN 'DATABASE_DATA_PLATFORM'
    WHEN 'DEV_ENGINEERING' THEN 'DEV_ENGINEERING'
    WHEN 'DEV_ENV' THEN 'DEV_ENGINEERING'
    WHEN 'DEV_ENV_ISSUE' THEN 'DEV_ENGINEERING'
    WHEN 'ITSM_PROCESS' THEN 'ITSM_PROCESS'
    WHEN 'ASSET_PROCUREMENT' THEN 'ASSET_PROCUREMENT'
    WHEN 'CHANGE_RELEASE' THEN 'CHANGE_RELEASE'
    WHEN 'POLICY_COMPLIANCE' THEN 'POLICY_COMPLIANCE'
    WHEN 'GENERAL_FAQ' THEN 'GENERAL_FAQ'
    WHEN 'OTHER' THEN 'OTHER'
    ELSE 'OTHER'
END
WHERE category IS NOT NULL;
